<template>
  <div class="main-content">
    <template v-if="showFlag">

      <div class="search-box-card">
        <el-form :inline="true" :model="searchForm" @submit.native.prevent="search()">
          <el-form-item label="账号">
            <el-input v-model="searchForm.zhanghao" placeholder="请输入账号" clearable size="medium"></el-input>
          </el-form-item>
          <el-form-item label="姓名">
            <el-input v-model="searchForm.xingming" placeholder="请输入姓名" clearable size="medium"></el-input>
          </el-form-item>

          <el-form-item>
            <el-button type="primary" icon="el-icon-search" @click="search()" size="medium">查询</el-button>
            <el-button icon="el-icon-refresh" @click="resetForm" size="medium">重置</el-button>

            <el-button v-if="isAuth('yonghu','新增')" type="success" icon="el-icon-plus" @click="addOrUpdateHandler()" size="medium">新增用户</el-button>
            <el-button v-if="isAuth('yonghu','删除')" type="danger" icon="el-icon-delete" :disabled="dataListSelections.length <= 0" @click="deleteHandler()" size="medium">批量删除</el-button>
          </el-form-item>
        </el-form>
      </div>

      <div class="table-box-card">
        <el-table
            :data="dataList"
            border
            v-loading="dataListLoading"
            @selection-change="selectionChangeHandler"
            style="width: 100%;">

          <el-table-column type="selection" header-align="center" align="center" width="50"></el-table-column>
          <el-table-column type="index" header-align="center" align="center" label="序号" width="60"></el-table-column>

          <el-table-column prop="zhanghao" header-align="center" align="center" label="账号" min-width="100"></el-table-column>
          <el-table-column prop="xingming" header-align="center" align="center" label="姓名" min-width="100"></el-table-column>
          <el-table-column prop="nianling" header-align="center" align="center" label="年龄" width="80"></el-table-column>

          <el-table-column prop="xingbie" header-align="center" align="center" label="性别" width="80">
            <template slot-scope="scope">
              <el-tag :type="scope.row.xingbie === '男' ? '' : 'danger'" effect="plain" size="small">{{ scope.row.xingbie }}</el-tag>
            </template>
          </el-table-column>

          <el-table-column prop="shenfenzheng" header-align="center" align="center" label="身份证" min-width="160"></el-table-column>
          <el-table-column prop="shouji" header-align="center" align="center" label="手机" min-width="120"></el-table-column>

          <el-table-column prop="touxiang" header-align="center" align="center" width="100" label="头像">
            <template slot-scope="scope">
              <div v-if="scope.row.touxiang">
                <el-image
                    class="table-img"
                    :src="scope.row.touxiang.split(',')[0].indexOf('http') == 0 ? scope.row.touxiang.split(',')[0] : $base.url + scope.row.touxiang.split(',')[0]"
                    :preview-src-list="[scope.row.touxiang.split(',')[0].indexOf('http') == 0 ? scope.row.touxiang.split(',')[0] : $base.url + scope.row.touxiang.split(',')[0]]">
                </el-image>
              </div>
              <div v-else>
                <el-avatar icon="el-icon-user-solid" size="small"></el-avatar>
              </div>
            </template>
          </el-table-column>

          <el-table-column header-align="center" align="center" width="200" label="操作" fixed="right">
            <template slot-scope="scope">
              <el-button v-if="isAuth('yonghu','查看')" type="text" icon="el-icon-view" size="small" @click="addOrUpdateHandler(scope.row.id,'info')">详情</el-button>
              <el-button v-if="isAuth('yonghu','修改')" type="text" icon="el-icon-edit" size="small" @click="addOrUpdateHandler(scope.row.id)">修改</el-button>
              <el-button v-if="isAuth('yonghu','删除')" type="text" icon="el-icon-delete" class="text-danger" size="small" @click="deleteHandler(scope.row.id)">删除</el-button>
            </template>
          </el-table-column>
        </el-table>

        <el-pagination
            @size-change="sizeChangeHandle"
            @current-change="currentChangeHandle"
            :current-page="pageIndex"
            :page-sizes="[10, 20, 50, 100]"
            :page-size="pageSize"
            :total="totalPage"
            layout="total, sizes, prev, pager, next, jumper"
            background
            class="pagination-content">
        </el-pagination>
      </div>
    </template>

    <add-or-update v-if="addOrUpdateFlag" :parent="this" ref="addOrUpdate"></add-or-update>
  </div>
</template>

<script>
import AddOrUpdate from "./add-or-update";
export default {
  data() {
    return {
      searchForm: {
        zhanghao: "",
        xingming: ""
      },
      dataList: [],
      pageIndex: 1,
      pageSize: 10,
      totalPage: 0,
      dataListLoading: false,
      dataListSelections: [],
      showFlag: true,
      addOrUpdateFlag: false,
    };
  },
  components: {
    AddOrUpdate,
  },
  created() {
    this.getDataList();
  },
  methods: {
    // 重置搜索
    resetForm() {
      this.searchForm.zhanghao = '';
      this.searchForm.xingming = '';
      this.search();
    },
    search() {
      this.pageIndex = 1;
      this.getDataList();
    },
    // 获取数据列表
    getDataList() {
      this.dataListLoading = true;
      let params = {
        page: this.pageIndex,
        limit: this.pageSize,
        sort: 'id',
        order: 'desc',
      }
      if(this.searchForm.zhanghao){
        params['zhanghao'] = '%' + this.searchForm.zhanghao + '%'
      }
      if(this.searchForm.xingming){
        params['xingming'] = '%' + this.searchForm.xingming + '%'
      }
      this.$http({
        url: "yonghu/page",
        method: "get",
        params: params
      }).then(({ data }) => {
        if (data && data.code === 0) {
          this.dataList = data.data.list;
          this.totalPage = data.data.total;
        } else {
          this.dataList = [];
          this.totalPage = 0;
        }
        this.dataListLoading = false;
      });
    },
    // 每页数
    sizeChangeHandle(val) {
      this.pageSize = val;
      this.pageIndex = 1;
      this.getDataList();
    },
    // 当前页
    currentChangeHandle(val) {
      this.pageIndex = val;
      this.getDataList();
    },
    // 多选
    selectionChangeHandler(val) {
      this.dataListSelections = val;
    },
    // 添加/修改
    addOrUpdateHandler(id,type) {
      this.showFlag = false;
      this.addOrUpdateFlag = true;
      this.crossAddOrUpdateFlag = false;
      if(type!='info'){
        type = 'else';
      }
      this.$nextTick(() => {
        this.$refs.addOrUpdate.init(id,type);
      });
    },
    // 删除
    deleteHandler(id) {
      var ids = id ? [Number(id)] : this.dataListSelections.map(item => Number(item.id));
      this.$confirm(`确定进行[${id ? "删除" : "批量删除"}]操作?`, "提示", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning"
      }).then(() => {
        this.$http({
          url: "yonghu/delete",
          method: "post",
          data: ids
        }).then(({ data }) => {
          if (data && data.code === 0) {
            this.$message.success("操作成功");
            this.search();
          } else {
            this.$message.error(data.msg);
          }
        });
      });
    },
  }
};
</script>

